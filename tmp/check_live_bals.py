import sqlite3
c = sqlite3.connect('crypto_bot.db')
q = c.cursor()
print("----- ETH BOTS -----")
q.execute("SELECT b.id, b.name, b.direction, b.status, t.total_invested, t.avg_entry_price, t.current_step FROM bots b LEFT JOIN trades t ON b.id=t.bot_id WHERE b.name LIKE '%eth%'")
for r in q.fetchall(): print(r)
print("----- BTC BOTS -----")
q.execute("SELECT b.id, b.name, b.direction, b.status, t.total_invested, t.avg_entry_price, t.current_step FROM bots b LEFT JOIN trades t ON b.id=t.bot_id WHERE b.name LIKE '%btc%'")
for r in q.fetchall(): print(r)
c.close()
